package looping;

public class ForTest {
    /*
       ================= Looping (Iteration) ======================
        # use to do any task again and again.

        1>  for
        2> while
        3> do-while

        =========================== for loop ======================
        syntax :

             for(initialization; condition; inc(++)/dec(--)){

                     //statements
             }

     */

    public static void main(String[] args) {

        //i++ => i = i+1

        for(int i=1;  i<=2000; i++) {
                  System.out.println("Good Morning");
        }


    }
}
